package Básico;

public class MediaSalarial {
    public static void main(String[] args) {

        //Calcular média de 4 salários

        double salario1 = 1500, salario2 = 1350, salario3 = 1200, salario4 = 1300, media;

        media = (salario1 + salario2 + salario3 + salario4) / 4;

        System.out.println("A média dos salários é: R$" + media);
    }
}
